package org.testframe.api.random;

/**
 * Represents the two sides of a coin, heads or tails.
 * @author Alonso del Arte
 */
public enum CoinSide {
    
    /**
     * The heads side, or obverse, of a coin. May be mapped to either true or 
     * false, as needed.
     */
    HEADS, 
    
    /**
     * The tails side, or reverse, of a coin. May be mapped to either true or 
     * false, as needed.
     */
    TAILS

}
